    private void jButton_clcActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_clcActionPerformed
        if(jCheckBox_Fruits.isSelected()|| jCheckBox_Veges.isSelected() || jCheckBox_Pizza.isSelected() ||
           jCheckBox_Wafers.isSelected() || jCheckBox_Biscuits.isSelected())
        {
        double f=Double.parseDouble(jTextField_Fruits.getText())*50;
        double v=Double.parseDouble(jTextField_Veges.getText())*50;
        double p=Double.parseDouble(jTextField_Pizza.getText())*50;
        double w=Double.parseDouble(jTextField_Wafers.getText())*50;
        double b=Double.parseDouble(jTextField_Biscuits.getText())*50;
        
        double totalitems= f+v+p+w+b;
        double del=totalitems*5/100;
        double mil= del*50/100;
        double subtotal=totalitems+del+mil;
        double cgst=subtotal*9/100;
        double sgst=cgst;
        double tax=cgst+sgst;
        double amount=subtotal+tax;
        
        String item=String.format("%.2f", totalitems);
        jTextField_TCI.setText((String)item);
        
        String d=String.format("%.2f", del);
        jTextField_TDC.setText((String)d);
        
        String m=String.format("%.2f", mil);
        jTextField_TCM.setText((String)m);
        
        String c=String.format("%.2f", cgst);
        jTextField_CGST.setText((String)c);
        
        String s=String.format("%.2f", sgst);
        jTextField_SGST.setText((String)s);
        
        String sub=String.format("%.2f", subtotal);
        jTextField_ST.setText((String)sub);
        
        String ta=String.format("%.2f", tax);
        jTextField_TAX.setText((String)ta);
        
        String tamount=String.format("%.2f", amount);
        jTextField_TA.setText((String)tamount);
        
        
            
        }
        else
        {
        txtDisplay.setText("Enter a choise");
        }
    }//GEN-LAST:event_jButton_clcActionPerformed

