    private void jButton_ansActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_ansActionPerformed
        secondnum = Double.parseDouble(txtDisplay.getText());
        if(operations == "+"){
        result=firstnum+secondnum;
        answer=String.format("%2f",result);
        txtDisplay.setText(answer);
        }
        else if(operations =="-"){
        result=firstnum-secondnum;
        answer=String.format("%2f",result);
        txtDisplay.setText(answer);
        }
        else if(operations =="*"){
        result=firstnum*secondnum;
        answer=String.format("%2f",result);
        txtDisplay.setText(answer);
        }
        else if(operations =="/"){
        result=firstnum/secondnum;
        answer=String.format("%2f",result);
        txtDisplay.setText(answer);
        }
        else {
        result=firstnum%secondnum;
        answer=String.format("%2f",result);
        txtDisplay.setText(answer);
        }
    }//GEN-LAST:event_jButton_ansActionPerformed

